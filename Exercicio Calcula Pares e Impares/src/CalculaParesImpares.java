public class CalculaParesImpares {
	private int numero;
	private int par;
	private int impar;

	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int getPar() {
		return par;
	}
	public void setPar(int par) {
		this.par = par;
	}

	public int getImpar() {
		return impar;
	}
	public void setImpar(int impar) {
		this.impar = impar;
	}

}
